package com.tellyouiam.literals.primitivetypes.integerliterals;
/**
 * Also new to Java 7 is the addition of binary literals. Binary literals can use only the
 * digits 0 and 1. Binary literals must start with either 0B or 0b
 * */
public class BinaryLiteral {
    int b1 = 0B101010;
    int b2 = 0b00011;
}
